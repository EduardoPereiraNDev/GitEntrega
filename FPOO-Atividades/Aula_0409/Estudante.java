package Aula_0409;

public class Estudante {
	public String nome;
	public int ra;
	public String curso;
	
	public Estudante() {
		
	}
	public Estudante(String nome, int ra, String curso) {
		this.nome = nome;
		this.ra = ra;
		this.curso = curso;
	}
	
	public String getNome() {
		return curso;
	}
	
	public int getRa() {
		return ra;
	}
	
	public String getCurso() {
		return curso;
	}
	
	public void setNome() {
		this.nome = nome;
	}
	
	public void setRa() {
		this.ra = ra;
	}
	
	public void setCurso() {
		this.curso = curso;
	}
}
